package cat;
public class Cat {
    String name;
    double age;
    double weight;

    double length;

    String color;

    String gender;

    public Cat(String name, double age, double weight, String color, double length, String gender) {
        this.name = name;
        this.age = age;
        this.weight = weight;
        this.color = color;
        this.length = length;
        this.gender = gender;
    }
}